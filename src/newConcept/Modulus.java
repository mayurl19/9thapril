 package newConcept;

public class Modulus {

	public static void main(String[] args) {
		int a= 10;
		int b=2;
		System.out.println( a % b); //  ' %' shows us only remainder value in console window
		
		int c=7;
		c %=8;
		System.out.println( c% 8); //   ' % 'shows us only remainder value in console window
		
		float d =29;
		d /=23;
		System.out.println( d/23); //    '/' shows us only quotient value in console window and we use in int or float
		
		int e = 26;
		++e;
		System.out.println(e); // '++e' increases one value of variable e.
		
		int f= 67;
		--f;
		System.out.println(f);  // '--f' decreases one value of variable f.
		
		int g=76;
		g +=56;
		System.out.println(g);   //  '+='  simply shows add of two no.
		
		int h=87;
		h-=89;
		System.out.println(h);   //  '-='simply  shows sub of two no.
		
		int i= 56;
		i *=65;
		System.out.println(i);  // '*='simply multiple of two no.	
		
	}

}
